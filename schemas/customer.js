const Schema = require('mongoose').Schema;

const CustomerSchema = Schema({
    name: {
        type: String,
        required: true
    },
    username: {
        type: String,
        required: true
    },
    email: {
        type: String,
        required: true
    },
    address: {
        type: String,
        required: true
    }
})

module.exports = new Schema(CustomerSchema);
